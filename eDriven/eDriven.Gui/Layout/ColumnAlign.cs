namespace eDriven.Gui.Layout
{
    public enum ColumnAlign
    {
        /// <summary>
        /// Do not justify the rows
        /// </summary>
        Left,

        /// <summary>
        /// Justify the rows by increasing the vertical gap
        /// </summary>
        JustifyUsingGap,

        /// <summary>
        /// Justify the rows by increasing the row height
        /// </summary>
        JustifyUsingWidth
    }
}
